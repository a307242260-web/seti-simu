    function enumerateSimulationTurnActions(workingRoot) {
      const currentPlayer = players.getCurrentPlayer(workingRoot?.playerState);
      if (!currentPlayer || !isAiAutoBattlePlayer(currentPlayer.id)) return [];
      if (isActionEffectFlowActive(workingRoot) || hasActivePendingSubFlow()) return [];
      const result = dispatchRuntimeAction?.(workingRoot, { kind: "standard_enumerate" });
      if (!result?.ok) return [];
      return (result.candidates || [])
        .filter((standardAction) => standardAction.phase !== "conditional")
        .map((standardAction) => ({
          id: standardAction.family,
          kind: standardAction.phase,
          family: standardAction.family,
          actionId: standardAction.actionId,
          actorId: standardAction.actorId,
          target: structuredClone(standardAction.target || null),
          payload: structuredClone(standardAction.payload || {}),
          standardAction: structuredClone(standardAction),
          available: true,
          label: standardAction.summary,
        }));
    }

    function resetGameForAiAutoBattle(options = {}) {
      resetAiStrategyDemandCache();
      const requestedActivePlayerCount = options.activePlayerCount == null
        ? DEFAULT_ACTIVE_PLAYER_COUNT
        : options.activePlayerCount;
      const activePlayerCount = Math.min(
        Math.max(1, Math.round(Number(requestedActivePlayerCount) || DEFAULT_ACTIVE_PLAYER_COUNT)),
        players.PLAYER_COLOR_IDS.length,
      );
      if (options.clearLogs !== false) {
        aiAutoBattleState.logs = [];
        aiAutoBattleState.bugs = [];
        aiAutoBattleState.bugCounts = {};
        aiAutoBattleState.lastSummary = null;
      }
      aiAutoBattleState.turnMoveCounts = {};
      aiAutoBattleState.turnCardCornerMoveCounts = {};
      const resetResult = startNewGame({
        activePlayerCount,
        clearStorage: false,
        message: "AI 自动对战新局已重置",
      });
      if (!resetResult.ok) throw new Error(`Composition AI 新局重置失败：${resetResult.code || "unknown"}`);
      const resetReadout = getRuleReadout();
      if (!resetReadout?.turnState) {
        throw new Error(`AI 新局 StateSource readout 缺少 turnState：${Object.keys(resetReadout || {}).join(",")}`);
      }
      return {
        ok: true,
        activePlayerCount,
        playerIds: [...resetReadout.turnState.activePlayerIds],
        message: "AI 自动对战新局已重置",
      };
    }


